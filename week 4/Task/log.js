
let bool1 = true;
let bool2 = false;

// Logical AND (&&)
console.log(`${bool1} && ${bool2} is ${bool1 && bool2}`);

// Logical OR (||)
console.log(`${bool1} || ${bool2} is ${bool1 || bool2}`);

// Logical NOT (!)
console.log(`!${bool1} is ${!bool1}`);
console.log(`!${bool2} is ${!bool2}`);

