
let num1 = 20;
let num2 = 5;

// Equal to
console.log(`${num1} == ${num2} is ${num1 == num2}`);

// Not equal to
console.log(`${num1} != ${num2} is ${num1 != num2}`);

// Greater than
console.log(`${num1} > ${num2} is ${num1 > num2}`);

// Less than
console.log(`${num1} < ${num2} is ${num1 < num2}`);

// Greater than or equal to
console.log(`${num1} >= ${num2} is ${num1 >= num2}`);

// Less than or equal to
console.log(`${num1} <= ${num2} is ${num1 <= num2}`);


